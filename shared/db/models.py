async def init_db(db_path: str) -> None:
    """Initialize database — create all tables and indexes.

    Safe to call multiple times (IF NOT EXISTS).
    Must be called once at application startup.

    Args:
        db_path: Full path to SQLite database file.
    """
    global _db_path
    _db_path = db_path

    # Ensure db directory exists
    from pathlib import Path

    Path(db_path).parent.mkdir(parents=True, exist_ok=True)

    async with aiosqlite.connect(db_path) as db:
        # Enable WAL mode for better concurrent read performance
        await db.execute("PRAGMA journal_mode=WAL;")

        # Create tables
        await db.execute(_TRADES_TABLE)
        await db.execute(_SIGNALS_TABLE)
        await db.execute(_DAILY_KPIS_TABLE)
        await db.execute(_WEIGHT_HISTORY_TABLE)
        await db.execute(_INGESTION_LOG_TABLE)
        await db.execute(_ALERTS_SENT_TABLE)

        # Create indexes
        for idx_sql in _INDEXES:
            await db.execute(idx_sql)

        await db.commit()

    logger.info(
        "Database initialized",
        db_path=db_path,
        tables=6,
        indexes=len(_INDEXES),
    )


def get_db() -> aiosqlite.Connection:
    """Get async database connection as context manager.

    Usage:
        async with get_db() as db:
            cursor = await db.execute("SELECT ...", (param,))
            rows = await cursor.fetchall()

    Returns:
        aiosqlite connection context manager.

    Raises:
        RuntimeError: If init_db() hasn't been called yet.
    """
    if _db_path is None:
        raise RuntimeError("Database not initialized. Call init_db() first.")
    return aiosqlite.connect(_db_path)


async def log_ingestion(
    *,
    system: str,
    timestamp: str,
    provider: str,
    module: str,
    status: str,
    latency_ms: int,
    data_points: int = 0,
    error: str | None = None,
    fallback_used: str | None = None,
) -> None:
    """Insert a row into ingestion_log.

    Every provider call should log through this for health monitoring.
    """
    async with get_db() as db:
        await db.execute(
            """
            INSERT INTO ingestion_log
                (system, timestamp, provider, module, status, latency_ms,
                 data_points, error, fallback_used)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
            """,
            (
                system,
                timestamp,
                provider,
                module,
                status,
                latency_ms,
                data_points,
                error,
                fallback_used,
            ),
        )
        await db.commit()


async def prune_old_data() -> dict:
    """Prune stale data based on retention policy.

    Retention:
        - ingestion_log: 30 days
        - alerts_sent: 60 days
        - signals: 90 days
        - trades, daily_kpis, weight_history: forever

    Returns:
        Dict with row counts deleted per table.
    """
    results = {}

    async with get_db() as db:
        for table, days in [
            ("ingestion_log", 30),
            ("alerts_sent", 60),
            ("signals", 90),
        ]:
            cursor = await db.execute(
                f"DELETE FROM {table} WHERE created_at < datetime('now', ?)",
                (f"-{days} days",),
            )
            results[table] = cursor.rowcount

        await db.commit()

    logger.info("Data pruning completed", pruned=results)
    return results
