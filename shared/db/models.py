"""
Database initialization and access layer.

SQLite via aiosqlite. Raw parameterized SQL only — no ORM.
All tables created with IF NOT EXISTS for idempotent init.
Both systems share one DB file with 'system' column for isolation.

Usage:
    from shared.db.models import init_db, get_db

    # At startup
    await init_db("/path/to/trading.db")

    # In modules
    async with get_db() as db:
        await db.execute("INSERT INTO trades ...", params)
"""

